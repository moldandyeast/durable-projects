#!/usr/bin/env node
/**
 * Emit src/project-runtime-bundled.ts for the Worker to serve at GET /project-runtime.js.
 */
import * as esbuild from "esbuild";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const result = await esbuild.build({
  entryPoints: [join(root, "src/client/project-runtime.ts")],
  bundle: true,
  minify: true,
  format: "iife",
  platform: "browser",
  target: ["es2022"],
  write: false,
});

const code = result.outputFiles[0].text;
const outPath = join(root, "src/project-runtime-bundled.ts");
writeFileSync(
  outPath,
  `/**\n * Generated by npm run build:client — do not edit.\n * Source: src/client/project-runtime.ts\n */\nexport const PROJECT_RUNTIME_JS = ${JSON.stringify(code)};\n`,
);
console.warn("wrote", outPath, `(${code.length} bytes)`);
